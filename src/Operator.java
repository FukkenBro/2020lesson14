
public class Operator {

    public int sum(int a, int b) {
        return a + b;
    }

    public int sub(int a, int b) {
        return a - b;
    }

    public double mult(int a, int b) {
        return (double) a * b;
    }

    public double div(int a, int b) {
        return (double) a / b;
    }

}
